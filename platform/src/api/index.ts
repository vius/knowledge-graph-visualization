import { useToast } from '@/components/ui/toast/use-toast'
const { toast } = useToast()
export const login = async (params: any) => {
    const res = await fetch('/api/platform/login', {
        method: 'POST',
        body: JSON.stringify(params),
        headers: {
            'Content-Type': 'application/json'
        }
    })
    const data = await res.json()
    if (res.status > 201) {
        const { message = '登录失败' } = data
        toast({
            title: message,
            duration: 3000,
            variant: 'destructive',
        });
        throw new Error(message)
    }
    return data
}

export const signup = async (params: any) => {
    const res = await fetch('/api/platform/signup', {
        method: 'POST',
        body: JSON.stringify(params),
        headers: {
            'Content-Type': 'application/json'
        }
    })
    const data = await res.json()
    if (res.status > 201) {
        const { message = '注册失败' } = data
        toast({
            description: message,
            variant: 'destructive',
            class: 'py-4 pl-4 pr-8'
        });
        throw new Error(message)
    }
    return data
}

export const getUserInfo = async () => {
    const res = await fetch('/api/platform/getUserInfo', {
        method: 'GET'
    })
    const data = await res.json()
    if (data.errNo !== 100) {
        return data.data || {}
    }
    return null
}

